export const getProjectPageBySlug = (slug) =>
  projectPagesData.find((project) => project.slug === slug);

export const getProjectPageByPath = (path) =>
  projectPagesData.find((project) => project.path === path);

export const getProjectFilmPaths = (project) =>
  [
    project.relatedFilmPath,
    ...(project.relatedFilmPaths || []),
    ...(project.relatedServicePaths || []),
  ].filter(Boolean);

export const getProjectLocalPaths = (project) =>
  [project.relatedLocalPath, ...(project.relatedLocalPaths || [])].filter(Boolean);

export const getProjectRegion = (project) => project.region || project.city;

export const getProjectServiceFilters = (project) =>
  project.serviceFilters?.length ? project.serviceFilters : [project.serviceType].filter(Boolean);

export const getProjectsByFilmPath = (filmPath) =>
  projectPagesData.filter((project) => getProjectFilmPaths(project).includes(filmPath));

export const getProjectsByLocalPath = (localPath, city) =>
  projectPagesData.filter((project) => {
    const matchesLocalPath = localPath && getProjectLocalPaths(project).includes(localPath);
    const matchesCity = city && (project.city === city || getProjectRegion(project) === city);

    return matchesLocalPath || matchesCity;
  });

export const getFeaturedProjects = (limit = 4) =>
  projectPagesData.filter((project) => project.featured).slice(0, limit);
